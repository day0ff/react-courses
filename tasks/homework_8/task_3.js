function check(str) {
  const array = str.split(' ');
  return array.sort((a, b) => {
    const digitalRootA = digitalRoot(a);
    const digitalRootB = digitalRoot(b);
    const result = compare(digitalRootA, digitalRootB);
    if (result === 0) return compare(a, b);
    return result;
  });
}

function digitalRoot(element) {
  return element.split('').reduce((acc, value) => acc + +value, 0);
}

function compare(a, b) {
  if (a > b) return 1;
  else if (a === b) return 0;
  else return -1;
}